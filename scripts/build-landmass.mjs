/* build-landmass.mjs — one-shot generator for the binary earth bitmap
   ─────────────────────────────────────────────────────────────────
   fetches natural earth 110m land geojson (public domain), rasterises
   it via a scanline even-odd fill at 720×360, packs to a base64 string,
   and emits src/modules/radio/landmass.ts as a self-contained constant.
   runs once; the output is committed so the app needs no runtime fetch.

     usage:  node scripts/build-landmass.mjs
*/

import { writeFile } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const SOURCE_URL =
  'https://raw.githubusercontent.com/nvkelso/natural-earth-vector/master/geojson/ne_110m_land.geojson'

const W = 720
const H = 360

const __dirname = dirname(fileURLToPath(import.meta.url))
const OUT = resolve(__dirname, '..', 'src', 'modules', 'radio', 'landmass.ts')

async function main() {
  console.log(`fetching ${SOURCE_URL}…`)
  const res = await fetch(SOURCE_URL)
  if (!res.ok) throw new Error(`source fetch failed: ${res.status} ${res.statusText}`)
  const gj = await res.json()
  console.log(`  → ${gj.features.length} features`)

  // collect every edge [(lon1, lat1), (lon2, lat2)] across every ring of every
  // feature. even-odd fill treats outer rings + holes uniformly.
  const edges = []
  for (const feat of gj.features) {
    const g = feat.geometry
    if (!g) continue
    const polys =
      g.type === 'Polygon' ? [g.coordinates] :
      g.type === 'MultiPolygon' ? g.coordinates :
      null
    if (!polys) continue
    for (const poly of polys) {
      for (const ring of poly) {
        for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
          const [x1, y1] = ring[j]
          const [x2, y2] = ring[i]
          edges.push([x1, y1, x2, y2])
        }
      }
    }
  }
  console.log(`  → ${edges.length} edges`)

  // raster: row-major, 1 bit per pixel (packed below).
  const raster = new Uint8Array(W * H)

  // scanline loop. for each row we compute the pixel-centre latitude, collect
  // every edge intersection x at that latitude, sort, then pair-fill.
  for (let y = 0; y < H; y++) {
    const lat = 90 - (y + 0.5) * (180 / H)
    const xs = []
    for (let e = 0; e < edges.length; e++) {
      const x1 = edges[e][0], y1 = edges[e][1]
      const x2 = edges[e][2], y2 = edges[e][3]
      const a = y1 > lat
      const b = y2 > lat
      if (a !== b) {
        // linear interp along the edge to the scanline
        const x = x1 + ((lat - y1) / (y2 - y1)) * (x2 - x1)
        xs.push(x)
      }
    }
    if (xs.length === 0) continue
    xs.sort((a, b) => a - b)
    // pair-fill: (xs[0], xs[1]), (xs[2], xs[3]), …
    for (let i = 0; i + 1 < xs.length; i += 2) {
      const lonA = xs[i], lonB = xs[i + 1]
      const cA = Math.max(0, Math.floor((lonA + 180) * (W / 360)))
      const cB = Math.min(W - 1, Math.ceil((lonB + 180) * (W / 360)) - 1)
      for (let c = cA; c <= cB; c++) raster[y * W + c] = 1
    }
  }

  // pack to 1 bit per pixel, msb-first within each byte
  const bytes = new Uint8Array(Math.ceil((W * H) / 8))
  let landPx = 0
  for (let i = 0; i < W * H; i++) {
    if (raster[i]) {
      bytes[i >> 3] |= 1 << (7 - (i & 7))
      landPx++
    }
  }
  const fraction = (landPx / (W * H)) * 100
  console.log(`  → land fraction: ${fraction.toFixed(2)}%  (expected ~29%)`)

  const b64 = Buffer.from(bytes).toString('base64')

  const ts = `/* generated by scripts/build-landmass.mjs — do not edit by hand.
   source: natural earth 110m land (public domain), rasterised to
   ${W}×${H} at 0.5° per pixel via scanline even-odd fill. 1 bit per
   pixel, row-major, msb-first within each byte. row 0 = north pole,
   row ${H - 1} = south pole; col 0 = 180°W, col ${W - 1} = 180°E−ε. */

export const LAND_W = ${W}
export const LAND_H = ${H}

const BASE64 =
${chunked(b64, 76).map((l) => `  '${l}'`).join(' +\n')}

/* decode base64 → packed Uint8Array once at module load. ~32 KB. */
function decode(s: string): Uint8Array {
  const bin = atob(s)
  const out = new Uint8Array(bin.length)
  for (let i = 0; i < bin.length; i++) out[i] = bin.charCodeAt(i)
  return out
}

export const LAND: Uint8Array = decode(BASE64)

/* is (lat, lon) over land? lat ∈ [-90, 90], lon ∈ [-180, 180].
   returns false at the poles beyond the grid, at invalid inputs. */
export function sampleLand(lat: number, lon: number): boolean {
  if (!Number.isFinite(lat) || !Number.isFinite(lon)) return false
  let col = Math.floor((lon + 180) * (LAND_W / 360))
  if (col < 0) col = 0
  else if (col >= LAND_W) col = LAND_W - 1
  let row = Math.floor((90 - lat) * (LAND_H / 180))
  if (row < 0) row = 0
  else if (row >= LAND_H) row = LAND_H - 1
  const i = row * LAND_W + col
  return (LAND[i >> 3] & (1 << (7 - (i & 7)))) !== 0
}
`

  await writeFile(OUT, ts, 'utf8')
  console.log(`wrote ${OUT}  (${Math.round(ts.length / 1024)} KB source, ${Math.round(bytes.length / 1024)} KB raw bitmap)`)
}

function chunked(s, n) {
  const out = []
  for (let i = 0; i < s.length; i += n) out.push(s.slice(i, i + n))
  return out
}

main().catch((e) => { console.error(e); process.exit(1) })
